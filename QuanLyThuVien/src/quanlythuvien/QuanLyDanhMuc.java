    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        // get the model from the jtable
        DefaultTableModel model = (DefaultTableModel)tblData.getModel();

        // get the selected row index
        int selectedRowIndex = tblData.getSelectedRow();

        // set the selected row data into jtextfields
        txtMa.setText(model.getValueAt(selectedRowIndex, 0).toString());
        txtTen.setText(model.getValueAt(selectedRowIndex, 1).toString());
    }//GEN-LAST:event_tblDataMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String name = txtTen.getText();

        Category c = new Category(0, name);

        int result = catImpl.add(c);
        if(result > 0) {
            Helpers.MessageBox("Success", "Add successfully", "success");
            loadData();
        } else {
            Helpers.MessageBox("False", "Opps! Something went wrong", "error");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int id = Integer.parseInt(txtMa.getText());
        Category c = new Category(id, "");
        int result = catImpl.delete(c);
        if(result > 0) {
            Helpers.MessageBox("Success", "Delete successfully", "success");
            loadData();
        } else {
            Helpers.MessageBox("False", "Opps! Something went wrong", "error");
        }
    }//GEN-LAST:event_btnXoaActionPerformed
    public void loadData() {
        Category[] categories = catImpl.list();
        
        DefaultTableModel dtm = (DefaultTableModel)tblData.getModel();
        dtm.setRowCount(0);
        for(Category c: categories) {
            dtm.addRow(new Object[] {c.id, c.categoryName});
        }
    }
    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        int id = Integer.parseInt(txtMa.getText());
        String name = txtTen.getText();
        Category c = new Category(id, name);

        int result = catImpl.update(c);
        if(result > 0) {
            Helpers.MessageBox("Success", "Update successfully", "success");
            loadData();
        } else {
            Helpers.MessageBox("False", "Opps! Something went wrong", "error");
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

